#include <stddef.h>
#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	char	x;

	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr_fd(nb / 10, fd);
	x = (nb % 10) + '0';
	write(fd, &x, 1);
}
