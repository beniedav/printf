#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ptrs;

	i = 0;
	ptrs = (unsigned char *)s;
	while (i < n)
	{
		ptrs[i] = 0;
		i++;
	}
}
